""" 
    Então, já vimos o que são variáveis, os tipos de dados numericos e os tipos de dados de textos e agora vemos ver um método para verificar qual o tipo de dado eu estou usando.
    Para isso basta eu usar a função  type().
    Ex.:
"""
a = "Ola Mundo"
b = 5
c = 3.5
d = 1+2j

print(type(a))
print(type(b))
